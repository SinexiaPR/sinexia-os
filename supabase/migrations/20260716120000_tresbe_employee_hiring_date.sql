DO $$
DECLARE
  v_total INTEGER;
BEGIN
  SELECT count(*) INTO v_total FROM tresbe_hiring_dates_20260716;
  IF v_total <> 281 THEN
    RAISE EXCEPTION 'Tresbe hiring date sheet validation failed: expected 281 rows, found %', v_total;
  END IF;
END;
$$;

WITH candidates AS (
  SELECT DISTINCT
    sheet.normalized_name AS sheet_key,
    employee.id AS employee_id
  FROM tresbe_hiring_dates_20260716 sheet
  JOIN public.tresbe_employees employee
    ON employee.is_active
   AND public.is_tresbe_company(employee.company_id)
   AND (
     employee.normalized_name = sheet.normalized_name
     OR EXISTS (
       SELECT 1
       FROM public.tresbe_employee_aliases alias
       WHERE alias.employee_id = employee.id
         AND alias.normalized_alias = sheet.normalized_name
     )
   )
), counted AS (
  SELECT
    sheet_key,
    employee_id,
    count(*) OVER (PARTITION BY sheet_key) AS sheet_match_count,
    count(*) OVER (PARTITION BY employee_id) AS employee_match_count
  FROM candidates
)
UPDATE public.tresbe_employees employee
SET hiring_date = sheet.hiring_date
FROM counted match
JOIN tresbe_hiring_dates_20260716 sheet ON sheet.normalized_name = match.sheet_key
WHERE employee.id = match.employee_id
  AND match.sheet_match_count = 1
  AND match.employee_match_count = 1;
