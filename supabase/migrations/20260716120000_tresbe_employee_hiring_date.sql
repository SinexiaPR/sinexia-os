-- vacation/sick accrual in a separate module) and backfills it for currently
-- active employees from the "Hiring Days Tresbe" workbook. Only updates
-- employees that already exist in the system; never inserts new employees.
-- Employees with no unambiguous name match, or whose sheet row had no valid
-- date, are left with hiring_date = NULL for manual entry via the employee
-- edit form.

ALTER TABLE public.tresbe_employees
  ADD COLUMN IF NOT EXISTS hiring_date DATE;

